type I18nContextValue = {
    lang: Lang;
    setLang: (l: Lang) => void;
    t: (k: string) => string;
};

const I18nContext = createContext<I18nContextValue>({
    lang: "es",
    setLang: () => { },
    t: () => ""
});

export const I18nProvider = ({ children, defaultLang = "es" as Lang }: { children: React.ReactNode, defaultLang?: Lang }) => {
    const [lang, setLang] = useState<Lang>(defaultLang);

    useEffect(() => {
        const loadLanguage = async () => {
            try {
                // Intentar obtener idioma del usuario desde API
                const response = await fetch('/api/settings/language');
                const data = await response.json();
                if (data.language && ['es', 'en', 'fr', 'de'].includes(data.language)) {
                    setLang(data.language as Lang);
                    return;
                }
            } catch (error) {
                // Sin fallback a localStorage
                console.error('Error loading language:', error);
            }

            // Fallback a idioma del navegador
            if (typeof window !== "undefined") {
                const browser = navigator.language?.slice(0, 2);
                if (browser && (browser === "es" || browser === "en" || browser === "fr" || browser === "de")) {
                    setLang(browser as Lang);
                }
            }
        };

        loadLanguage();
    }, []);

    const t = (key: string) => {
        return translations[lang][key] || translations['es'][key] || key;
    };

    const changeLang = async (l: Lang) => {
        setLang(l);
        // Guardar en la API si el usuario est√° logueado
        try {
            await fetch('/api/settings/language', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ language: l })
            });
        } catch (error) {
            // Silently fail - no problem if not saved
        }
    };

    return (
        <I18nContext.Provider value={{ lang, setLang: changeLang, t }}>
            {children}
        </I18nContext.Provider>
    );
};

export const useTranslation = () => useContext(I18nContext);